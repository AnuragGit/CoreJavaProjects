package com.anurag.staticImport;

import static java.lang.System.out;
/**
 * @author Anurag
 * 
 *    Concept:- Static import in Java allows to import static members of class and use them, 
 *              as they are declared in the same class.


 *
 */
public class StaticImport1 {
	public static void main(String[] args) {
		out.println("Hello World!");
	}
}

/*
		Output:-
		
		Hello World!

*/