package com.anurag.array.multidimentional;
class Multidimentional27 
{
	public static void main(String[] args) 
	{
		int x[][]= new int[4][];
		x[0]= new int[10];
		x[1]= new int[1];
		x[2]= new int[20];
		x[3]=new int[4];
		System.out.println(x[3][3]);
		System.out.println("Hello World!");
	}
}
