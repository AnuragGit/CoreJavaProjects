package com.anurag.array;
/**
 * 
 * @author Anurag
 *         Concept:- By variable we can store only one value .
 *         To store Multiple value in single variable we require array.
 */
public class Array1 
{
	public static void main(String[] args) 
	{
		int x[]= new int[2];
		x[0]=1;
		x[1]=2;
		System.out.println(x[0]);
		System.out.println(x[1]);
		System.out.println("Hello World!");
	}
}
/*
Output:-

1
2
Hello World!*/