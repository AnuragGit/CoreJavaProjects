package com.anurag.initial;

public class Basic2 {
	public static void main(String[] args) {
		System.out.println(1000);//This is integer
		System.out.println(10.9090); //This is float
		System.out.println('s');// This is character
		System.out.println(true);// This is boolean
		System.out.println("end");// This is string
	}
}
